// Represents a runner with name, age (in years), bib number, and time
// (in minutes).
class Runner{
    String name;
    Integer age;
    Integer bib;
    Integer time;
    Runner(String name, Integer age, Integer bib, Integer time) {
        this.name = name;
        this.age = age;
        this.bib = bib;
        this.time = time;
    }
}